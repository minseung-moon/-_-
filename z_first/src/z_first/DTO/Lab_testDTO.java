package z_first.DTO;

public class Lab_testDTO {
	private String code;
	private String name;
	
	public Lab_testDTO() {
		super();
	}
	public Lab_testDTO(String code, String name) {
		super();
		this.code = code;
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
